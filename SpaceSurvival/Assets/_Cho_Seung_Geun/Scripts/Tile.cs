using System.Collections;
using System.Collections.Generic;
using UnityEngine;

[SelectionBase]                         // 오브젝트 클릭 시 자식 오브젝트가 아닌 이 클래스가 들어있는 오브젝트가 클릭되도록 만드는 어트리뷰트
public class Tile : MonoBehaviour
{
    /// <summary>
    /// 타일의 타입
    /// </summary>
    public enum MapTileType
    {
        centerTile = 0,
        sideTile,
        vertexTile
    }

    /// <summary>
    /// 이 타일 위에 놓여져 있는 물체의 타입
    /// </summary>
    public enum ExistType
    {
        None = 0,
        monster,
        item,
        prop
    }

    // 타일 타입
    public MapTileType tileType = 0;
    public MapTileType TileType
    {
        get => tileType;
        set
        {
            tileType = value;
        }
    }

    // 타일 위 몬스터, 아이템 등 타입 존재 여부
    public ExistType existType = 0;
    public ExistType ExistTypes
    {
        get => existType;
        set
        {
            existType = value;
        }
    }

    // 타일의 가로 인덱스
    int width = 0;
    public int Width
    {
        get => width;
        set
        {
            width = value;
        }
    }

    // 타일의 세로 인덱스
    int length = 0;
    public int Length
    {
        get => length;
        set
        {
            length = value;
        }
    }
}
