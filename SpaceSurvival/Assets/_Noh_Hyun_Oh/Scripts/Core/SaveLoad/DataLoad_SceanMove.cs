using StructList;
using System;
using System.Collections;
using System.Collections.Generic;
using UnityEngine;

/// <summary>
/// 로드 했을때 씬연결시 처리할 내용 정의하기위해 따로뺏다.
/// </summary>
public class DataLoad_SceanMove : MonoBehaviour
{
    
    
   

    private void Start()
    {
        SaveLoadManager.Instance.loadedSceanMove = FileLoadAction;
    }
    /// <summary>
    /// 로드 눌렀을때 화면이동과 데이터 셋팅 함수
    /// </summary>
    /// <param name="data">로드된 데이터</param>
    private void FileLoadAction(JsonGameData data)
    {
        //여기에 파싱작업이필요하다 실제로사용되는 작업
        if (data != null)
        {
            SaveLoadManager.Instance.ParsingProcess.LoadParsing(data); //저장된 데이터 셋팅 
            Debug.Log($"{data} 파일이 정상로드됬습니다 , {data.SceanName} 파싱작업후 맵이동 작성을 해야하니 맵이 필요합니다.");
            LoadingScean.SceanLoading(data.SceanName);
            if (TurnManager.Instance.TurnIndex > 0) //턴진행중이면 배틀맵임으로 
            {
                SpaceSurvival_GameManager.Instance.BattleMapInitClass.TestReset(); //데이터 초기화하고 
                SpaceSurvival_GameManager.Instance.BattleMapInitClass.TestInit(); //다시 셋팅하고 
            }

        }
    }
 
}